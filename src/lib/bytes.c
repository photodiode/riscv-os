
#include <types.h>


u16 swap_u16(const u16 v) {
	return (
		((v & (0xffU<<0)) << 8) |
		((v & (0xffU<<8)) >> 8)
	);
}

u32 swap_u32(const u32 v) {
	return (
		((v & (0xffU<< 0)) << 24) |
		((v & (0xffU<< 8)) <<  8) |
		((v & (0xffU<<16)) >>  8) |
		((v & (0xffU<<24)) >> 24)
	);
}

u64 swap_u64(const u64 v) {
	return (
		((v & (0xffUL<< 0)) << 56) |
		((v & (0xffUL<< 8)) << 48) |
		((v & (0xffUL<<16)) << 40) |
		((v & (0xffUL<<24)) << 32) |
		((v & (0xffUL<<32)) >> 32) |
		((v & (0xffUL<<40)) >> 40) |
		((v & (0xffUL<<48)) >> 48) |
		((v & (0xffUL<<56)) >> 56)
	);
}

u64 trailing_zeroes_u64(const u64 i) {
	static const u8 tab64[64] = {
		 0,  1,  2, 53,  3,  7, 54, 27,  4, 38, 41,  8, 34, 55, 48, 28,
		62,  5, 39, 46, 44, 42, 22,  9, 24, 35, 59, 56, 49, 18, 29, 11,
		63, 52,  6, 26, 37, 40, 33, 47, 61, 45, 43, 21, 23, 58, 17, 10,
		51, 25, 36, 32, 60, 20, 57, 16, 50, 31, 19, 15, 30, 14, 13, 12
	};
	return tab64[((u64)((i & -i) * 0x022fdd63cc95386dUL)) >> 58];
}
